  /**
   * 页面的初始数据
   */
  data: {
    goods: []
  },

  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    fetch(`/shops/${options.item}`)
      .then(value => {
        this.setData({ goods: value.data })
        wx.setNavigationBarTitle({ title: value.data.name })
      })
  },
  previewHandle(e) {
    wx.previewImage({
      current: e.target.dataset.src,
      urls: this.data.goods.images
    })
  },
